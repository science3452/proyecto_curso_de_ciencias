    <h1>REINO PROTISTA</h1>
    <br>
    <h2>¿QUÉ ES EL REINO PROTISTA?</h2>
    <br>
    <p>El reino protista, también llamado protoctista, es uno de los grupos en que la biología clasifica los seres vivos eucariotas que no pueden ser clasificados como animales, plantas ni hongos.</p>

    <p>El reino protista es un grupo parafilético (no contiene a todos los descendientes de un antepasado común) y agrupa a un conjunto muy diverso de organismos generalmente unicelulares o pluricelulares simples que no forman tejidos, tanto autótrofos como heterótrofos. Su enorme variedad hace que sea difícil caracterizarlos, excepto en los rasgos comunes de todo ser eucariótico, o sea, poseedor de células con un núcleo celular definido.</p>

    <p>La existencia de un reino protista fue propuesta en 1969 en la teoría de los cinco reinos de la vida, pero actualmente se considera un término en desuso y se tiende a clasificar a sus miembros integrantes dentro de las demás ramas de la vida eucariótica.</p>

