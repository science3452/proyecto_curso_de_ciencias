    <br>
    <h2>¿QUÉ ES EL REINO FUNGI?</h2>
    <br>
    <p>El reino fungi es uno de los grupos en que la biología clasifica a las formas de vida conocidas. Está compuesto por más de 144.000 especies diferentes de hongos, entre los que figuran las levaduras, los mohos y las setas, y que comparten características fundamentales como la inmovilidad, la alimentación heterótrofa y ciertas estructuras celulares.</p>

    <p>Los hongos existen en todo el mundo y en distintos hábitats, y aparecen en distintas formas y presentaciones.</p>

    <p>De la totalidad de los hongos que habitan nuestro planeta, tan solo un 5 % ha sido estudiado y clasificado, y se estima que existen alrededor de 1,5 millones de especies aún desconocidas. Esto en parte se debe a que antiguamente los hongos eran clasificados como un tipo de planta, hasta que en el siglo XIX se los empezó a distinguir como un reino biológico aparte.</p>

    <p>La ciencia que se especializa en los miembros del reino fungi se llama micología.</p>

