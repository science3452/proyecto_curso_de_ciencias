    <h1>REINO VEGETAL</h1>
    <br>
    <h2>¿QUÉ ES EL REINO VEGETAL?</h2>
    <br>
    <p>Se llama Reino plantae (del latín “plantas”) o Reino vegetal a uno de los principales grupos en que la biología clasifica a los seres vivos conocidos. Junto con el Reino animal (animalia), el Reino de los hongos (fungi), el Reino protista (protistae) y el Reino moneras (monera), constituyen las categorías tradicionales de clasificación de la vida.</p>

    <p>El reino vegetal es de los más antiguos que el ser humano ha estudiado, a pesar de que no exista un criterio estricto y universal respecto a qué considerar como una planta y qué no. En líneas generales se considera de este grupo a todas las formas de vida vegetal terrestre (árboles, arbustos, hierba, etc.) y a todas las algas acuáticas, siempre y cuando lleven a cabo alguna forma de nutrición fotosintética.</p>

    <p>Los miembros de este reino descienden, en teoría, de un grupo de seres vivos llamados Primoplantae o Archaeplastida: los primeros eucariotas capaces de realizar la fotosíntesis, y cuya aparición en la Tierra primitiva se estima hace unos 1.200 millones de años, durante el Eón Proterozoico.</p>

