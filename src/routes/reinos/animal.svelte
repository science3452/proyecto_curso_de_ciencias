    <h2>¿QUÉ ES EL REINO ANIMAL?</h2>
    <br>
    <p>El reino animal o animalia constituye, junto al reino vegetal, fungi, protistas y moneras, uno de los posibles en que la biología clasifica a las formas de vida conocida. Es uno de los grandes reinos tradicionales, a pesar de que la clasificación ha variado notoriamente a lo largo de más de 200 años de intentos de clasificación.</p>

    <p>Las criaturas contenidas en este reino se llaman animales, y se caracterizan por tener una enorme diversidad ecológica, morfológica y conductual, ya que se hallan presentes a lo largo y ancho del planeta. Al mismo tiempo, se distinguen de los otros reinos eucariotas por carecer de clorofila (no hacen fotosíntesis) y pared celular (presente en células de plantas y hongos), así como por su reproducción casi enteramente sexual y su capacidad de movimiento autónomo y voluntario.</p>

    <p>Al reino animal pertenecen alrededor de dos millones de especies distintas en todo el mundo, agrupados en varios taxones o filos, y en dos grandes categorías: vertebrados e invertebrados. En este reino, además, se clasifica también clasificado el ser humano.</p>

