    <h1>tipos de celula</h1>
    <div>
        <h1>CELULA PROCARIOTA</h1>
        <p>Las células procariotas tienen una estructura básica sencilla, sin membrana nuclear, por lo que su material genético se encuentra disperso, ocupando un espacio llamado nucleoide, y que está en contacto directo con el resto del citoplasma.</p>
        <p>Las células procariotas son pequeñas y tienen un tamaño de entre 1-5 µm. Fueron las primeras formas de vida en la Tierra y hasta donde se conoce, todos los seres vivos formados por células procariotas son unicelulares.</p>
    </div>
    <div>
        <h1>CELULA EUCARIOTA</h1>
        <p>Las células eucariotas tienen una estructura más compleja que las procariotas. Tienen el núcleo rodeado de una membrana nuclear, por lo que su material genético queda contenido en el núcleo. Además, estas células poseen orgánulos (también llamados “organelas”) en su citoplasma que pueden estar delimitados por membranas.</p>
        <p>El tamaño de las células eucariotas varía entre 10-100 µm, por lo que son más grandes que las células procariotas. En la historia evolutiva de la Tierra, las células eucariotas surgieron después que las procariotas.</p>
    </div>
    <div>
        <h1>LA CELULA VEGETAL</h1>
        <center><mark>La celula vegetal es de tipo eucariota</mark></center>
        <p>Tienen un núcleo rodeado de membrana nuclear donde se encuentra el material genético.</p>
        <p>Poseen una pared celular rígida compuesta principalmente por celulosa. Esta estructura le da forma a la célula y le otorga sostén a la planta (los organismos vegetales no tienen esqueletos como los animales).</p>
    </div>
    <div>
        <h1>CELULA ANIMAL</h1>
        <center><mark>La celula animal es de tipo eucariota.</mark></center>
        <p>Están constituidas por una membrana plasmática y distintas organelas (complejo de Golgi, mitocondrias, ribosomas, lisosomas, retículo endoplasmático liso y rugoso, entre otros).</p>
        <p>El material genético se encuentra en un núcleo envuelto en una membrana nuclear.</p>
        <p>No tienen pared celular y presentan formas muy diversas.</p>
        <p>Poseen lisosomas (organelas exclusivas de las células animales), que son vesículas que contienen enzimas digestivas y se dedican a degradar ciertos compuestos químicos y estructuras celulares.</p>
