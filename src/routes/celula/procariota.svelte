    <h1>CELULA PROCARIOTA</h1>

    <p>Las células procariotas tienen una estructura básica sencilla, sin membrana nuclear, por lo que su material genético se encuentra disperso, ocupando un espacio llamado nucleoide, y que está en contacto directo con el resto del citoplasma.</p>

    <p>Las células procariotas son pequeñas y tienen un tamaño de entre 1-5 µm. Fueron las primeras formas de vida en la Tierra y hasta donde se conoce, todos los seres vivos formados por células procariotas son unicelulares.</p>
